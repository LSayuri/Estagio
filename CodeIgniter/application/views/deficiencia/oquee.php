		<BLOCKQUOTE>
			<p>É classificado em 3 graus atualmente: autismo leve, autismo moderado, autismo severo.<br>
			Autismo é uma severa desordem no desenvolvimento , inicia-se desde o nascimento, ou se desenvolve nos primeiros dois anos e meio de 
			vida.A maioria das crianças autistas são, aparentemente, perfeitamente normais, mas passam o tempo envolvidas em comportamentos 
			ininteligíveis e hiper-ativos, completamente distintos do comportamento de crianças típicas.<br>
			A idade em que começa a intervenção tem um impacto direto nos resultados. Quanto mais cedo uma criança começa a ser tratada melhor será 
			seu prognóstico. Recentemente há um aumento na porcentagem de crianças que freqüentam a escola em salas de aula convencionais e vivem 
			de forma semi-independente em comunidades. Entretanto, a maioria das pessoas autistas permanence com déficits em suas habilidades de 
			comunicação e socialização.</p>
