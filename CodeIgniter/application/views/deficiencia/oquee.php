		<h3>PRINCIPAIS TIPOS DE DEFICIÊNCIA INTELECTUAL</h3>

		<u>-Síndrome de Down</u>
		<BLOCKQUOTE>
		
			<p>Alteração genética que ocorre na formação do bebê, no início da gravidez. O grau de deficiência intelectual provocado pela síndrome 
			é variável, e o coeficiente de inteligência (QI) pode variar e chegar a valores inferiores a 40. A linguagem fica mais comprometida, 
			mas a visão é relativamente preservada. As interações sociais podem se desenvolver bem, no entanto podem aparecer distúrbios como 
			hiperatividade, depressão, entre outros.</p>
		</BLOCKQUOTE>
		
		<u>-Síndrome do X-Frágil</u>
		<BLOCKQUOTE>
		
			<p>Alteração genética que provoca atraso mental. A criança apresenta face alongada, orelhas grandes ou salientes, além de 
			comprometimento ocular e comportamento social atípico, principalmente timidez.</p>
		</BLOCKQUOTE>
		
		<u>-Síndrome de Prader-Willi</u>
		<BLOCKQUOTE>
		
			<p>O quadro clínico varia de paciente a paciente, conforme a idade. No período neonatal, a criança apresenta severa hipotonia 
			muscular, baixo peso e pequena estatura. Em geral a pessoa apresenta problemas de aprendizagem e dificuldade para pensamentos e 
			conceitos abstratos.</p>
		</BLOCKQUOTE>
		
		<u>-Síndrome de Angelman</u>
		<BLOCKQUOTE>
		
			<p>Distúrbio neurológico que causa deficiência intelectual, comprometimento ou ausência de fala, epilepsia, atraso psicomotor, andar 
			desequilibrado, com as pernas afastadas e esticadas, sono entrecortado e difícil, alterações no comportamento, entre outras.</p>
		</BLOCKQUOTE>
		
		<u>-Síndrome Williams</u> 
		<BLOCKQUOTE>
		
			<p>Alteração genética que causa deficiência intelectual de leve a moderada. A pessoa apresenta comprometimento maior da capacidade 
			visual e espacial em contraste com um bom desenvolvimento da linguagem oral e na música.</p>
		</BLOCKQUOTE>

		<u>-Erros Inatos de Metabolismo(Fenilcetonúria, Hipotireoidismo congênito etc.)</u>
		<BLOCKQUOTE>
			<p>Alterações metabólicas, em geral enzimáticas, que normalmente não apresentam sinais nem sintomas sugestivos de doenças. São 
			detectados pelo Teste do Pezinho, e quando tratados adequadamente, podem prevenir o aparecimento de deficiência intelectual. Alguns 
			achados clínicos ou laboratoriais que sugerem esse tipo de distúrbio metabólico: falha de crescimento adequado, doenças recorrentes e 
			inexplicáveis, convulsões, atoxia, perda de habilidade psicomotora, hipotonia, sonolência anormal ou coma, anormalidade ocular, sexual, 
			de pelos e cabelos, surdez inexplicada, acidose láctea e/ou metabólica, distúrbios de colesterol, entre outros.</p>
		</BLOCKQUOTE>

