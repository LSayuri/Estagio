		<h3>TRABALHO REALIZADO</h3>
		<br>
		<p>A fundação Varginhense de Assistência aos Excepcionais - FUVAE é uma entidade filantrópica sem fins lucrativos de direito privado, responsável pelo atendimento de Pessoas com Deficiência Intelectual, desde 0 ano até o fim da vida.<br> A FUVAE possui atualmente 334 alunos que possuem entre outros: Deficiência mental (leve, moderada e profunda), Deficiências múltiplas, Síndrome de Down, Paralisia Cerebral, Autismo, Deficiência Visual, etc.<br>A Fundação oferece aos usuários acesso à educação e saúde de forma adaptada, ou seja, com equipamentos, pessoal e recursos especiais dos tradicionalmente utilizados, para melhor atender os alunos, conforme suas características, além de oficinas pedagógicas de formação e capacitação profissional que possibilitam a inclusão da pessoa deficiente no mercado de trabalho. Ou seja, fornece um atendimento e assessoramento através de habilitação e reabilitação das pessoas com deficiência e da promoção de sua inclusão à vida comunitária.<br></p>
	</div>

	<div class="topico">
	
		<p>O grupo que compõe o quadro de funcionários que realizam todo trabalho de aprimoramento e desenvolvimento da melhora intelectual e 
		motora dos alunos da Fundação, envolvem:</p><br>
		<p>- Psicólogas<br>- Fisioterapeutas<br>- Terapeutas Ocupacionais<br>- Assistentes Sociais, Médicos (Neuropediatra, Neurologista, 
		Psiquiatra)<br>- Fonoaudiólogas<br>- Nutricionista<br>- Auxiliares de Enfermagem<br>- Professores<br>- Auxiliares de Serviços Gerais</p>
		<br>
		<p>Para realizar e manter todas essas atividades e necessidades da FUVAE, cabe à Diretoria, através de convênios, subvenções, grupos de 
		sócios contribuintes, aluguéis, doações, organizando shows, rifas, entre outros, a obtenção de fundos para manutenção e conservação da 
		qualidade desses serviços prestados.</p>
