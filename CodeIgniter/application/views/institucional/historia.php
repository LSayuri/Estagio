		<h3>NOSSA HISTÓRIA</h3>
		<br>
		<p>A FUVAE - Fundação Varginhense de Assistência aos Excepcionais é uma fundação beneficente, com atuação nas áreas de 
assistência social, educação, saúde, prevenção, trabalho, profissionalização, defesa e garantia de direitos, esporte, cultura, 
lazer, estudo, pesquisa, meio ambiente e outros, sem fins lucrativos ou de fins não econômicos, com duração indeterminada. 	
A escola foi idealizada pelo casal Targino Hermógenes Nogueira Neto e Maria Aparecida Figueiredo Nogueira que convidaram outros 
casais Jairo Rezende Paiva e Nadia Barbosa Paiva, José Merberg Porto e Maria Célia Porto e em 01 de fevereiro de 1961 fundaram 
a Escola dos Excepcionais São Sebastião, hoje FUVAE – Fundação Varginhense de Assistência aos Excepcionais.<br>
Pais de filhos com deficiências físicas e mentais, os fundadores sentiram a necessidade de criar um ambiente específico onde essas crianças pudessem 
ser assistidas por pessoas capacitadas e interessadas em transmitir-lhes conhecimentos e desenvolver algumas habilidades.<br>
A primeira turma constitui-se de seis crianças, assistidas por uma única pedagoga, Filomena Ramos de Rezende. Com o crescimento do atendimento a 
estas crianças, outro prédio foi adquirido à Rua Major Evaristo Paiva, e reformado no ano de 1962, para atender a demanda. O serviço de atendimento 
técnico iniciou-se com psicóloga e neurologista. Em 1975, a Fundação transferiu-se para o prédio extinto “Orfanato Dr. José de Rezende Pinto”, 
desativado alguns anos antes, em regime de cessão, podendo assim, atender a um número maior de crianças especiais da cidade e região. No decorrer 
dos anos, outros técnicos foram incorporados ao quadro: pedagogo, assistente social, fonoaudióloga, médico-pediatra, fisioterapeuta, terapeuta 
ocupacional e dentista, formando-se assim, a Equipe Técnica Interdisciplinar e de Apoio Especializado, atuando junto aos alunos, família e 
comunidade.
		</p>
