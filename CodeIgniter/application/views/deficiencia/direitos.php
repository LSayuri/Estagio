		<h3>DIREITOS E BENEFÍCIOS</h3>
		
		<p>A pessoa com Deficiência Intelectual tem os mesmos direitos que todos os outros cidadãos, assegurados pela Constituição Federal do 
		nosso país: direito à vida, liberdade, igualdade, não discriminação, segurança, propriedade, educação, saúde, trabalho, moradia, lazer, 
		previdência e assistência social, entre outros.<br>
		Com a redução das fronteiras da desigualdade, a pessoa com deficiência poderá, por exemplo, ingressar no mercado de trabalho ou na rede 
		regular de ensino, sem qualquer espécie de discriminação, e assim mostrar sua produtividade e valor.<br>
		É dever da família estimular e uma obrigação das instituições especializadas capacitar a pessoa com deficiência, objetivando sua inclusão 
		nestes ambientes, onde terá direito a todos os benefícios assegurados aos demais trabalhadores e estudantes.</p>
