		<h3>Principais causas</h3>
		<p>Os fatores de risco e causas que podem levar à Deficiência Intelectual podem ocorrer em três fases: pré-natais, perinatais  e pós-natais.</p>
		
		<u>Pré-natais</u>
		<p>Fatores que incidem desde o momento da concepção do bebê até o início do trabalho de parto:</p>
		<BLOCKQUOTE>
		
			<p>Fatores genéticos<br>
			Alterações cromossômicas (numéricas ou estruturais) - provocam Síndrome de Down, entre outras.<br>
			Alterações gênicas (erros inatos do metabolismo): que provocam Fenilcetonúria, entre outras.</p>
		</BLOCKQUOTE>
		
		<p>Fatores que afetam o complexo materno-fetal.</p>
		<BLOCKQUOTE>
			<p>Tabagismo, alcoolismo, consumo de drogas, efeitos colaterais de medicamentos teratogênicos (capazes de provocar danos nos embriões e 
			fetos).<br> 
			Doenças maternas crônicas ou gestacionais (como diabetes mellitus). <br>
			Doenças infecciosas na mãe, que podem comprometer o feto: sífilis, rubéola, toxoplasmose. <br>
			Desnutrição materna.</p>
		</BLOCKQUOTE>
		
		<u>Perinatais</u>
		<p>Fatores que incidem do início do trabalho de parto até o 30.º dia de vida do bebê:</p> 
		<BLOCKQUOTE>
		
			<p>Hipóxia ou anoxia (oxigenação cerebral insuficiente). <br>
			Prematuridade e baixo peso: Pequeno para Idade Gestacional (PIG). Icterícia grave do recém-nascido (kernicterus).</p>
		</BLOCKQUOTE>
		
		<u>Pós-natais</u>
		<p>Fatores que incidem do 30.º dia de vida do bebê até o final da adolescência:</p>
		<BLOCKQUOTE>
		
			<p>Desnutrição, desidratação grave, carência de estimulação global. 
			Infecções: meningites, sarampo. Intoxicações exógenas: envenenamentos provocados por remédios, inseticidas, produtos químicos como 
			chumbo, mercúrio etc. Acidentes: trânsito, afogamento, choque elétrico, asfixia, quedas etc.</p>
		</BLOCKQUOTE>
