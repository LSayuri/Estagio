		<h3>COMO PREVENIR?</h3>
		
		<p>A chance de uma criança desenvolver Deficiência Intelectual depende de diversos fatores relacionados à genética, acompanhamento da 
		gestação, saúde da mãe durante a gravidez, ambiente familiar saudável na infância e adolescência da criança, entre outros.<br>
		Alguns cuidados devem ser tomados, para evitar ou minimizar as consequências da Deficiência Intelectual na vida da pessoa:</p>
		<BLOCKQUOTE>
		
			<p>-Procurar aconselhamento genético, antes de engravidar, quando houver casos de deficiência intelectual na família, casamentos entre 
			parentesou idade materna avançada (maior que 35 anos).<br>
			Fazer um acompanhamento pré-natal adequado para investigar possíveis infecções ou problemas maternos que podem ser tratados antes que 
			ocorram danos ao feto.<br>
			-Manter uma alimentação saudável durante a gestação e evitar uso de bebidas alcoólicas, tabaco e outras drogas. Realizar o Teste do 
			Pezinho – que é obrigatório no Brasil – assim que o bebê nascer. Esse teste é a maneira mais efetiva de detectar a fenilcetonúria e o 
			hipotireoidismo congênito, que se não forem devidamente tratados podem levar à Deficiência Intelectual.
			Seguir recomendações de vacinas.<br>
			-Oferecer ao bebê alimentação adequada e ambiente familiar saudável e estimulador, além de cuidados para tentar evitar acidentes na 
			infância.<br>
			-Procurar um médico caso note algum problema no desenvolvimento e/ou crescimento da criança.<br></p>
		</BLOCKQUOTE>
