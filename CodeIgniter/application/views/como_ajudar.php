		<div class="topico">
			
			<h3>Imposto Solidário</h3>
			<br>
			<p>Pessoas Físicas e Jurídicas podem contribuir com doações ou destinar parte de seu
imposto de renda devido permitidos por lei ao FIA - Fundo da Infância e Adolescência.<br>
O COMDEDICA – Conselho Municipal de Defesa dos Direitos da Criança e do
Adolescente é quem vai gerir os recursos alocados no FIA, de acordo com critérios
específicos, previamente aprovados por maioria absoluta em assembleia, para
programas das entidades governamentais ou não governamentais, em projetos de
defesa dos direitos das crianças e dos adolescentes. Faça a declaração no formulário
completo e a destinação deve ser feita no ano-base, ou seja até 31 de dezembro de
cada ano. A dedução dos valores ao Fundo da Infância e Adolescência (FIA) não
prejudica outras deduções, como aquelas relativas a dependentes, saúde, educação e
pensão.</p>
		</div>
	
		<div class="topico">
		
			<h3>Pessoas Físicas</h3>
			<br>
			<p>6% do Imposto de Renda Devido. A declaração tem que ser feita
no formulário completo.</p>

			<h3>Pessoas Jurídicas</h3>
			<br>
			<p>1% do Imposto de Renda Devido, tributados pelo lucro real. 
(No mês, Trimestre ou Ano) </p>
		</div>
	
		<div class="topico">
		
			<h3>Como Fazer a Doação</h3>
			<br>
			<p>1. Depositar o valor na conta do FIA Fundo da Infância e Adolescência.
(Banco do Brasil (001) Agência (0032-9) conta corrente (72.905-1)</p>
			<p>2. Fazer contato com o COMDEDICA – Conselho Municipal de Defesa dos Direitos
da Criança e do Adolescente e solicitar o recibo de doação. Informe seus dados
(Nome, endereço completo, comprovante de depósito e CPF ou CNPJ)</p>
			<p>3. Junto ao COMDEDICA faça o convênio escolhendo a entidade a ser destinada a 
doação.</p>
			<p>4. Ao preencher sua Declaração de Imposto de Renda, há um campo no formulário
onde deverá informar a data, o valor e o CNPJ do fundo onde o recurso foi
depositado, com isto o próprio programa da Receita Federal, já considera
automaticamente a renúncia fiscal.</p>
			<p>5. O recibo emitido pelo Conselho é o seu comprovante junto à Receita Federal.</p>
		</div>
		
