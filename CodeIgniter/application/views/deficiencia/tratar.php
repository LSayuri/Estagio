		<h3>COMO TRATAR?</h3>
		
		<p>A Deficiência Intelectual não é uma doença, mas uma limitação. A pessoa com Deficiência Intelectual deve receber acompanhamento médico 
		e estímulos, através de trabalhos terapêuticos com psicólogos, fonoaudiólogos e terapeutas ocupacionais.<br>
		De forma geral, a pessoa com Deficiência Intelectual tem, como qualquer outra, dificuldades e potencialidades. Seu acompanhamento consiste 
		em reforçar e favorecer o desenvolvimento destas potencialidades e proporcionar o apoio necessário às suas dificuldades garantindo seu 
		bem-estar e inclusão na sociedade.</p>
	</div>
	
	<div class="topico">
	
		<h3>A IMPORTÂNCIA DA INCLUSÃO</h3>
		<p>A inclusão social é um instrumento extremamente importante na determinação da qualidade de vida dessa pessoa, pois permite o acesso a 
		todos os recursos da comunidade, que favorecerão o seu desenvolvimento global, reforçarão a sua autonomia e ajudarão a construir a sua 
		cidadania.<br>
		Como qualquer um de nós, a pessoa com Deficiência Intelectual percebe tudo que se passa ao seu redor. Portanto, devemos criar as 
		oportunidades para que ela possa realizar todas as atividades que achar interessantes e auxiliá-la no que for possível.</p>
