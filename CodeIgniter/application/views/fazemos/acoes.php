		<h3> AÇÕES </h3><br>
		<p>A FUVAE vem desenvolvendo ações com intuito de cumprir sua missão, contribuir para a construção e efetivação de um sistema inclusivo, 
		acolher a diversidade e atuar no fomento de políticas de ação na comunidade varginhense, favorecendo, assim a garantia dos direitos das 
		pessoas com deficiência. 
		As ações são desenvolvidas nas seguintes áreas: SOCIAL, SAÚDE e EDUCAÇÃO.</p>
		
		<u><h4>SOCIAL:</h4></u><p> Os serviços sócio-assistenciais na FUVAE são organizados como Serviço de Proteção Social Especial de Média Complexidade, 
		sem fins lucrativos.</p>
		
		<p>No aspecto social a FUVAE busca desenvolver um trabalho em rede, através da comunicação de idéias e compromissos, articulando-se com a 
		sociedade, órgãos públicos e privados. Neste sentido ações são desenvolvidas como:</p><br>
		
		<p>-Parceria com empresas, escolas e demais segmentos da comunidade;<br>
		-Participação em projetos sociais de apoio às famílias como: PAA – Programa de Aquisição de Alimentos e PAIF – Programa de Atenção 
		Integral à Família;<br>
		-Participação em Conselhos, Seminários, Congressos e Fóruns em defesa dos direitos das pessoas com deficiências;<br>
		-Intermediação para a colocação de pessoas com deficiência no mercado de trabalho;<br>
		-Divulgação do potencial produtivo da pessoa com deficiência;<br>
		-Orientação bio-psico-social às famílias e comunidade;<br>
		-Divulgação de serviços especializados;<br>
		-Contato com Promotoria Pública, Conselho Tutelar;<br> 
		-Orientações e encaminhamentos a serviços prestados na comunidade;<br>
		-Transporte Escolar: Através de parcerias com a Secretaria de Habitação de Habitação e Promoção Social (SEHAD), viabilizou-se a aquisição 
		de carteiras de transporte coletivo para alunos da FUVAE e o Transporte Adaptado para alunos cadeirantes com direito a acompanhante;<br>
		-Implantação e gerenciamento de projetos, entre outros;</p>
	</div>
	
	<div class="topico">
	
		<u><h4>SAÚDE:</h4></u> <p>Com atividades de habilitação e reabilitação.</p>
		
		<p>Na área da saúde são desenvolvidas ações visando à prevenção de deficiências, habilitação e reabilitação das pessoas com deficiência. 
		São agendados para atendimentos pela Equipe Interdisciplinar, que é composta por: Assistentes Sociais, Psicólogas, Psicopedagogas, 
		Terapeutas Ocupacionais, Médico Neuropediatra, Médico Neurologista, Médica Psiquiatra, Fonoaudiólogas, Fisioterapeutas, Nutricionista, 
		Dentista, Pedagogas e Enfermeiros.<br>
		Após estudo de caso, que é realizado semanalmente, são encaminhados para atendimentos e apoios, se necessário, pela Assistente Social e 
		outro membro da Equipe Técnica da FUVAE, onde recebem orientações para procedimentos adequados que propiciem o desenvolvimento pleno do 
		aluno e sua qualidade de vida. Todas as ações são resolvidas em equipe, o serviço é gratuito.</p><br>
		
		<p>Para efetivação do trabalho são realizadas parcerias com secretarias municipais e estaduais de saúde, encaminhamento a centros 
		especializados, atendimento clínico e ambulatorial, como:</p><br>
		
		<p>-Participação no CMS – Conselho Municipal de Saúde;<br>
		-Encaminhamento de pacientes ao Genoma Humano;<br>
		-Encaminhamento de pacientes à AACD;<br>
		-Encaminhamentos para Rede Sarah de Hospitais;<br>
		-Encaminhamento para INTO/RJ;<br>
		-Contato com secretarias municipais e estaduais de saúde;<br>
		-Atendimento odontológico a alunos da FUVAE e a pessoas com deficiência da comunidade;<br>
		-Palestras informativas sobre prevenção de Câncer Bucal;<br>
		-Palestras informativas sobre alimentação saudável;<br>
		-Parceria com SENAC buscando a promoção de cursos para familiares sobre alimentação alternativa;<br>
		-Contato e parceria com médicos especializados do município para atendimento de cortesia em casos específicos;<br>
		-Adaptação de mobiliários para alunos da FUVAE e para os alunos que foram incluídos em outras escolas;<br>
		-Avaliações nutricionais e trabalho de reeducação alimentar;<br>
		-Interlocução entre os diversos setores clínicos da FUVAE;<br>
		-Avaliação diagnóstica realizada por equipe multidisciplinar;<br>
		-Avaliação e aplicação de toxina Botulínica em pacientes de Varginha e Região;</p>
	</div>
	
