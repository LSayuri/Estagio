		<h3>NOSSA MISSÃO E OBJETIVOS</h3>
		<br>
		<h4>Missão</h4>
		<p>Promover e articular ações de defesa de direitos e prevenção, orientações, prestação de serviços, apoio à família, direcionadas à 
		melhoria da qualidade de vida da pessoa com deficiência e à construção de uma sociedade justa e solidária.</p>
		<br>
		<h4>Objetivos</h4>
		<p>Promover a habilitação e a reabilitação da pessoa com deficiência intelectual e mÚltipla e a sua família, sua inclusão à vida 
		comunitária por meio de recursos e procedimentos das áreas de assistência social, saúde, educação, trabalho e outros, com vistas à 
		redução das incapacidades, desenvolvimento das potencialidades e habilidades para o trabalho, à inclusão social e à melhoria da qualidade 
		de vida a autonomia, a independência, a segurança, o acesso aos direitos e à participação plena e efetiva na sociedade.</p>
