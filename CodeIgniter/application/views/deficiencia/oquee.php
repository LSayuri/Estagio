		<h3>O QUE É?</h3>
		
		<img src=<?php echo base_url("assets/imgs/aaidd.jpg"); ?> width="200" style="position: relative; left: 60%; bottom: 10px;" alt="aaidd: Associação Americana de Deficiências Intelectual e de Desenvolvimento">
		
		<p>A Deficiência Intelectual, segundo a Associação Americana sobre Deficiência Intelectual do Desenvolvimento AAIDD, caracteriza-se por um 
		funcionamento intelectual inferior à média (QI), associado a limitações adaptativas em pelo menos duas áreas de habilidades (comunicação, 
		autocuidado, vida no lar, adaptação social, saúde e segurança, uso de recursos da comunidade, determinação, funções acadêmicas, lazer e 
		trabalho), que ocorrem antes dos 18 anos de idade.<br></p>
		<BLOCKQUOTE>
		-A habilidade conceitual é compreendida como aquela que faz referência as capacidades essenciais da inteligência, envolvendo suas grandezas 
		abstratas como, por exemplo, leitura e escrita, conceito de dinheiro e linguagem (receptiva e significativa).<br>
		</BLOCKQUOTE><BLOCKQUOTE>
		-A habilidade prática se refere à habilidade de se sustentar e de se manter como um indivíduo autônomo nas atividades corriqueiras da vida 
		diária, compreendendo, entre outros, as habilidades sensório-motoras, de auto-cuidado e segurança, de trabalho e descanso.<br>
		</BLOCKQUOTE><BLOCKQUOTE>
		-A habilidade social diz respeito à habilidade para compreender as perspectivas sobre condutas socialmente adequadas/aceitas, como, por 
		exemplo, responsabilidade, inocência (possibilidade de ser ludibriado e alvo de abuso e/ou violência) e auto-estima.</BLOCKQUOTE><br>
		<p>No dia a dia, isso significa que a pessoa com Deficiência Intelectual tem dificuldade para aprender, entender e realizar atividades comuns 
		para as outras pessoas. Muitas vezes, essa pessoa se comporta como se tivesse menos idade do que realmente tem.<br>
		A Deficiência Intelectual é resultado, quase sempre, de uma alteração no desempenho cerebral, provocada por fatores genéticos, distúrbios 
		na gestação, problemas no parto ou na vida após o nascimento. Um dos maiores desafios enfrentados pelos pesquisadores da área é que em 
		grande parte dos casos estudados essa alteração não tem uma causa conhecida ou identificada. Muitas vezes não se chega a estabelecer 
		claramente a origem da deficiência.</p>
